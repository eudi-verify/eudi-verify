import { readFile, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const packageDirs = ["server", "client", "embed", "react"];
const selectedDirs = process.argv.slice(2);
const repoRoot = join(dirname(fileURLToPath(import.meta.url)), "..");

for (const packageDir of selectedDirs.length > 0 ? selectedDirs : packageDirs) {
  if (!packageDirs.includes(packageDir)) {
    throw new Error(`Unknown package directory: ${packageDir}`);
  }

  const packageRoot = join(repoRoot, "packages", packageDir);
  const packageJson = JSON.parse(
    await readFile(join(packageRoot, "package.json"), "utf8"),
  );

  await writeFile(
    join(packageRoot, "src", "version.ts"),
    [
      "// Generated by scripts/generate-package-versions.mjs.",
      "// Do not edit by hand.",
      `export const VERSION = ${JSON.stringify(packageJson.version)} as const;`,
      "",
    ].join("\n"),
  );
}
